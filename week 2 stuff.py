def main():
    # Sum of two floating point numbers
    a = 3.7
    b = 2.1
    float_sum = a + b
    print(f"{a} + {b} = {float_sum}")
    print(f"Data type: {type(float_sum)}\n")

    # Difference between two integers
    x = 15
    y = 6
    int_diff = x - y
    print(f"{x} - {y} = {int_diff}")
    print(f"Data type: {type(int_diff)}\n")

    # Product of a floating point number and an integer
    c = 4.5
    n = 3
    mixed_product = c * n
    print(f"{c} * {n} = {mixed_product}")
    print(f"Data type: {type(mixed_product)}")


if __name__ == "__main__":
    main()